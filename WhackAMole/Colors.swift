import Foundation
import UIKit


class CustomColors {
    
    
    func lightGreen()  -> UIColor {
        return UIColor(red: 0/255, green: 255/255, blue: 24/255, alpha: 1)
        
    }
    
    func darkGreen() -> UIColor {
        return  UIColor(red: 9/255, green: 98/255, blue: 0/255, alpha: 1)
    }
    
    
    func lightBlue()  -> UIColor {
        return UIColor(red: 32/255, green: 170/255, blue: 247/255, alpha: 1)
    }
    
    
    func darkBlue() -> UIColor {
        return UIColor(red: 0/255, green: 122/255, blue: 255/255, alpha: 1)
    }
    
    
    func lightRed() -> UIColor {
        return UIColor(red: 215/255, green: 10/255, blue: 32/255, alpha: 1)
        
    }
    
    func darkRed() -> UIColor {
        return UIColor(red: 164/255, green: 7/255, blue: 23/255, alpha: 1)
        
    }
    
    func yellow() -> UIColor {
        return UIColor(red: 232/255, green: 234/255, blue: 10/255, alpha: 1)
        
    }
    
    func orange() -> UIColor {
        return UIColor(red: 1.0, green: 0, blue: 0, alpha: 1.0)
        
        
    }
    
    func offWhite() -> UIColor {
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1.0)
    }
    
    
}